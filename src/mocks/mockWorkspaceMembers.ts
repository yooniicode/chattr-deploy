const cloneMembersForWorkspace = (
  workspaceId: string,
  members: WorkspaceMember[],
  names: string[],
): WorkspaceMember[] =>
  members.map((member, index) => ({
    ...member,
    id: `${workspaceId}-${member.id}`,
    role: index === 0 ? 'admin' : member.role,
    user: {
      ...member.user,
      id: index === 0 ? currentUserId : `${workspaceId}-${member.user.id}`,
      email: index === 0 ? member.user.email : `${workspaceId}-${member.user.email}`,
      name: names[index] ?? member.user.name,
      status: index % 4 === 0 ? 'online' : member.user.status,
    },
  }))

